        <h3>How To Adopt a Kabul Critter</h3><span style=
        "font-size:6px;"><br /></span><span style=
        "font-size:13px;">If you would like to adopt a cat or dog that has been rescued from the streets of Kabul and nursed to good health at our shelter, we can arrange to send him or her to you. We will send you lots of photos and a full description of his or her personality. All our dogs are mixed breeds, but many have some some genetic traits from Afghan hunting dogs, which are sleek hounds with curly tails. Others have some Asian shepherd genes, which makes them larger and shaggier. All of the dogs we offer for adoption are healthy, spayed or neutered, vaccinated over a period of time and very well socialized to other dogs and people. Some do better with cats than others. <br /><br />
All of our services to animals in Kabul are free, but to ship a dog by commercial air cargo from Kabul to New York costs about $2500 and to ship a cat costs about $1700. We have some generous donors who are able to help with some shipping costs for individual dogs, and we are also periodically able to bring some back as accompanied baggage, which saves about half the cost. We know there are many dogs and cats in America who need loving homes, but there are also many shelters and other opportunities for them. In Afghanistan, where there are many homeless, neglected and abused street animals, it is extremely difficult to find them adoptive homes and there are no shelters for them. Our small facility is always full to the brim. Also, many of the dogs we have available have been rescued by American or British soldiers who were unable to take them home, but who wanted to make sure they were safe from harm. Please consider adopting one of these needy and grateful Kabul critters, and we will do our best to help you raise the money to bring them home.<br /><br />
If you would like to know more about adopting a dog, please email us at kabulcritters@gmail.com.


